fair feedback to the concerned faculty involved. Every time a feedback is given, a 
counter is stored in the database which gives the information about the number of 
students who have given feedback and the same counter is used to determine the 
average performance analysis. The aim of the project is to reduce the time for getting 
feedback data and provide an in depth analysis about the performance. The system so
developed helps in making the decision processes and the selection processes much 
more effective.
